def add_two_numbers(n1, n2):
    """ Add 2 numbers """
    return n1 + n2


def substract_two_numbers(n1, n2):
    """ Substract 2 numbers """
    return n1 - n2


def multiply_two_numbers(n1, n2):
    """ Multiply 2 numbers"""
    return n1 * n2


def division_two_numbers(n1, n2):
    """ Division 2 numbers"""
    return n1 / n2
